package com.newland.comp.bean.indicator;

public class Marking {
	private String marking_id;// 营销工作量id
	private String marking_name;// 营销工作量名称
	private String marking_value;// 营销工作量值

	public String getMarking_id() {
		return marking_id;
	}

	public void setMarking_id(String marking_id) {
		this.marking_id = marking_id;
	}

	public String getMarking_name() {
		return marking_name;
	}

	public void setMarking_name(String marking_name) {
		this.marking_name = marking_name;
	}

	public String getMarking_value() {
		return marking_value;
	}

	public void setMarking_value(String marking_value) {
		this.marking_value = marking_value;
	}

}
